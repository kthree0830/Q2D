- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender {
    UIViewController *vc = segue.destinationViewController;
    if ([segue.identifier isEqualToString:@"caijian"]) {
        vc.title = @"裁剪";
    } else if ([segue.identifier isEqualToString:@"jieping"]) {
        vc.title = @"截屏";
    } else if ([segue.identifier isEqualToString:@"shuiyin"]) {
        vc.title = @"水印";
    } else if ([segue.identifier isEqualToString:@"juxing"]) {
        vc.title = @"矩形";
    } else if ([segue.identifier isEqualToString:@"shanxing"]) {
        vc.title = @"扇形";
    }
}

